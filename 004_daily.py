""" Задача №836 "Чётно-нечётная" с acmp.ru
Задано n чисел a1, a2, ... , an. Выберите из них четные числа, у которых третья справа цифра в их представлении
в восьмеричной системе счисления нечетна. Выбранные числа отсортируйте по неубыванию и выведите в выходной файл.
Первая строка входного файла содержит целое число n (1 ≤ n ≤ 105). Вторая строка входного файла содержит n целых
чисел: a1, a2, ... , an. Они отделены друг от друга пробелами и заданы в десятичной системе счисления. Для всех 
i (1 ≤ i ≤ n ) верно неравенство 64 ≤ ai ≤ 109. В первой строке выходного файла OUTPUT.TXT выведите количество k 
искомых чисел. Во второй строке выведите эти числа в указанном порядке в десятичной системе счисления."""

n=int(input())
a=[int(s) for s in input().split()]
b=[]
k=0
for i in range(n):
    if ((a[i]%8**3)//8**2)%2==1 and a[i]%2==0:
        b.append(a[i])
        k+=1
print(k)
print(' '.join([str(s) for s in sorted(b)]))

""" Задача №98 "Игра в числа" с acmp.ru
Игра в числа ведётся на одномерном массиве целых положительных чисел. Перед началом, жеребьёвкой определяется,
кто будет ходить первым (первый игрок), а кто – вторым (второй игрок). Процесс игры состоит в том, что игроки
по очереди (сначала первый игрок, затем второй, следом опять первый и так далее) вычёркивают числа из массива.
Вычеркнуть можно только число, находящееся в конце или начале оставшегося массива. При этом всегда вычёркивается
максимальное число из этих двух. Если первое и последнее числа массива равны, то вычёркивается первое. Игра
продолжается до того момента, пока не будут вычеркнуты все числа. Каждое вычеркнутое число идёт в актив тому
игроку, который его вычеркнул. После окончания игры каждый игрок суммирует вычеркнутые им числа. Победителем 
объявляется тот, кто наберет больше очков. Некоторые игроки поняли, что результат не зависит от стратегии игры,
и решили попросить Вас написать программу для получения результата. В первой строке входного файла находится 
одно целое число N – количество чисел в массиве (1 ≤ N ≤ 104). Во второй строке находятся N целых положительных 
чисел из диапазона [1, 32000], разделённых пробелом. В выходной файл OUTPUT.TXT выведите два числа, разделенные 
двоеточием. Первое число – количество очков, набираемых первым игроком при игре на этом массиве, второе число – 
для второго."""

n=int(input())
a=[int(s) for s in input().split()]
b,e=0,n-1
p=[0]*2
for i in range(n):
    if a[b]<a[e]:
        p[i%2]+=a[e]
        e-=1
    else:
        p[i%2]+=a[b]
        b+=1
print(p[0],p[1],sep=':')
