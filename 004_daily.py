""" Задача №5 "Статистика" с acmp.ru
Вася не любит английский язык, но каждый раз старается получить хотя бы четверку за четверть, 
чтобы оставаться ударником. В текущей четверти Вася заметил следующую закономерность: по нечетным дням месяца 
он получал тройки, а по четным – четверки. Так же он помнит, в какие дни он получал эти оценки. Поэтому он 
выписал на бумажке все эти дни для того, чтобы оценить, сколько у него троек и сколько четверок. Помогите Васе 
это сделать, расположив четные и нечетные числа в разных строчках. Вася может рассчитывать на оценку 4, если 
четверок не меньше, чем троек. На входе:в первой строке записано единственное число N – количество элементов 
целочисленного массива (1 ≤ N ≤ 100). Вторая строка содержит N чисел, представляющих заданный массив. Каждый 
элемент массива – натуральное число от 1 до 31. Все элементы массива разделены пробелом. В первую строку 
выходного файла нужно вывести числа, которые соответствуют дням месяцев, в которые Вася получил тройки, а во 
второй строке соответственно расположить числа месяца, в которые Вася получил четверки. В третьей строке нужно 
вывести «YES»,если Вася может рассчитывать на четверку и «NO» в противном случае. В каждой строчке числа следует 
выводить в том же порядке, в котором они идут во входных данных. При выводе числа отделяются пробелом."""

n=int(input())
a=[s for s in input().split()]
o3=[i for i in a if int(i)%2==1]
print(' '.join(o3))
o4=[j for j in a if int(j)%2==0]
print (' '.join(o4))
if len(o3)>n-len(o3):
    print('NO')
else:
    print('YES')

